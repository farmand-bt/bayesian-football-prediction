    def _prepare_covariates(self, df):
        """Prepare all covariates for modeling"""

        print("\n" + "="*60)
        print("PREPARING ENHANCED COVARIATES")
        print("="*60)

        # Initialize covariate dictionaries
        self.team_covariates = {}
